#include "cub3d.h"

int	ft_check(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (0);
	while (str[i] != '\n' && str[i])
		i++;
	if (str[i] == 00)
		return (0);
	return (1);
}
